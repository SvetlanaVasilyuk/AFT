package Calculator;

public class CalculatorForPrimitives {

    public static int addition(int n1, int n2) {
        return n1 + n2;
    }

    public static int substraction(int n1, int n2) {
        return n1 - n2;
    }

    public static int multiplication(int n1, int n2) {
        return n1 * n2;
    }

    public static double division(int n1, int n2) {
        return (double) n1 / (double) n2;
    }

    public static int remainderOfDivision(int n1, int n2) {
        return n1 % n2;
    }

    public static double addition(double d1, double d2) {
        return d1 + d2;
    }

    public static double substraction(double d1, double d2) {
        return d1 - d2;
    }

    public static double multiplication(double d1, double d2) {
        return d1 * d2;
    }

    public static double division(double d1, double d2) {
        return d1 / d2;
    }

    public static double remainderOfDivision(double d1, double d2) {
        return d1 % d2;
    }
}
